package day05;

public class WhileLoop {
    public static void main(String[] args) {
        /**
         * Print integer value from 1 - 10 using while loop
         */
        int i = 0; //initialization

        while (i <= 10) {
            System.out.println("Value of i is :" + i);//1,2
            i++;
        }

    }
}
