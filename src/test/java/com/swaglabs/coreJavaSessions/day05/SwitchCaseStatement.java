package day05;

public class SwitchCaseStatement {
    public static void main(String[] args) {

        int age = 6;

        switch (age) {
            case 5:
                System.out.println("I am a kid");
                break;
            case 1:
                System.out.println("I am a kid");
                break;
            case 2:
                System.out.println("I am a kid");
                break;
            case 3:
                System.out.println("I am a kid");
                break;
            case 4:
                System.out.println("I am a kid");
                break;
            case 18:
                System.out.println("I am eligible for voting");
                break;
            default:
                System.out.println("Wrong input by user");

        }
    }
}
