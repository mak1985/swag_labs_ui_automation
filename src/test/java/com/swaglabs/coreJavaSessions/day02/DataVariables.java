package day02;

public class DataVariables {
    public static void main(String[] args) {

//        1. Declaration  - single line comments
        // 2. Initialization

        /** Multi-lines comments
         * 1. Declaration - To declare a variable int x
         * 2. Initialization - To assign a value to the variable x = 2;
         *
         * Declaration + Initialization
         * int x = 2; (numeric value)
         * String address  =  "Whitefield 560066";         (AlphaNumeric value)
         */

        int a = 2; //Declaration + Initialization
        System.out.println(a);//2

        //Re-assignment of a variable
        a = 4;
        System.out.println(a); //4

        a = 5;







    }
}
